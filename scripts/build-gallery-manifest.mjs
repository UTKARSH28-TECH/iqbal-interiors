// Build-time gallery manifest generator.
//
// Reads public/images/gallery in Node (where the filesystem exists) and writes
// a typed, bundled data module that the app imports at runtime. This is what
// makes the gallery work on Cloudflare Workers / OpenNext, which have no
// filesystem at request time.
//
// Runs automatically before `dev`, `build`, `preview`, and `deploy`
// (see package.json), and can be run manually via `npm run gallery:manifest`.

import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";
import { imageSize } from "image-size";

const scriptDir = path.dirname(fileURLToPath(import.meta.url));
const projectRoot = path.join(scriptDir, "..");
const GALLERY_ROOT = path.join(projectRoot, "public", "images", "gallery");
const OUT_FILE = path.join(projectRoot, "lib", "gallery-manifest.generated.ts");
const IMAGE_PATTERN = /\.(avif|webp|png|jpe?g)$/i;

function readDirSafe(dir) {
  try {
    return fs.readdirSync(dir);
  } catch {
    return [];
  }
}

function isDirectory(p) {
  try {
    return fs.statSync(p).isDirectory();
  } catch {
    return false;
  }
}

const slugs = readDirSafe(GALLERY_ROOT)
  .filter((name) => isDirectory(path.join(GALLERY_ROOT, name)))
  .sort();

const manifest = slugs.map((slug) => {
  const dir = path.join(GALLERY_ROOT, slug);
  const images = readDirSafe(dir)
    .filter((file) => IMAGE_PATTERN.test(file))
    .sort((a, b) => a.localeCompare(b, undefined, { numeric: true }))
    .map((file) => {
      let width = 0;
      let height = 0;
      try {
        const dimensions = imageSize(fs.readFileSync(path.join(dir, file)));
        width = dimensions.width ?? 0;
        height = dimensions.height ?? 0;
      } catch {
        // Dimensions stay 0; consumers fall back to a default ratio.
      }
      return {
        src: `/images/gallery/${slug}/${encodeURIComponent(file)}`,
        width,
        height,
      };
    });
  return { slug, images };
});

const fileContents =
  "// AUTO-GENERATED by scripts/build-gallery-manifest.mjs — do not edit by hand.\n" +
  "// Regenerate with `npm run gallery:manifest` (runs automatically on build).\n\n" +
  'import type { GalleryManifest } from "@/types/gallery";\n\n' +
  `export const galleryManifest: GalleryManifest = ${JSON.stringify(manifest, null, 2)};\n`;

fs.mkdirSync(path.dirname(OUT_FILE), { recursive: true });
fs.writeFileSync(OUT_FILE, fileContents);

const imageCount = manifest.reduce((total, c) => total + c.images.length, 0);
console.log(
  `gallery manifest: ${manifest.length} categories, ${imageCount} images -> ${path.relative(projectRoot, OUT_FILE)}`,
);

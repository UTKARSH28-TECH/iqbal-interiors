// Build-time video manifest generator.
//
// Scans public/videos for video files and pairs each with a poster image in
// public/images/video-thumbnails (matched by filename). Emits a typed, bundled
// data module so the runtime (Cloudflare Workers / OpenNext) never touches the
// filesystem — mirrors scripts/build-gallery-manifest.mjs.
//
// Runs automatically before dev/build/preview/deploy (see package.json) and can
// be run manually via `npm run videos:manifest`.

import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const scriptDir = path.dirname(fileURLToPath(import.meta.url));
const projectRoot = path.join(scriptDir, "..");
const VIDEO_ROOT = path.join(projectRoot, "public", "videos");
const THUMBNAIL_ROOT = path.join(
  projectRoot,
  "public",
  "images",
  "video-thumbnails",
);
const OUT_FILE = path.join(projectRoot, "lib", "video-manifest.generated.ts");
const VIDEO_PATTERN = /\.(mp4|webm|mov)$/i;
const POSTER_EXTENSIONS = [".jpg", ".jpeg", ".png", ".webp", ".avif"];

function readDirSafe(dir) {
  try {
    return fs.readdirSync(dir);
  } catch {
    return [];
  }
}

function fileExists(p) {
  try {
    return fs.statSync(p).isFile();
  } catch {
    return false;
  }
}

/** Find a poster image named after the video, in any supported extension. */
function findPoster(name) {
  for (const ext of POSTER_EXTENSIONS) {
    if (fileExists(path.join(THUMBNAIL_ROOT, `${name}${ext}`))) {
      return `/images/video-thumbnails/${name}${ext}`;
    }
  }
  return undefined;
}

const manifest = readDirSafe(VIDEO_ROOT)
  .filter((file) => VIDEO_PATTERN.test(file))
  // Cover first, then the rest naturally sorted by filename.
  .sort((a, b) => {
    const aCover = /^cover\./i.test(a);
    const bCover = /^cover\./i.test(b);
    if (aCover !== bCover) return aCover ? -1 : 1;
    return a.localeCompare(b, undefined, { numeric: true });
  })
  .map((file) => {
    const name = file.replace(VIDEO_PATTERN, "");
    const poster = findPoster(name);
    return {
      name,
      src: `/videos/${encodeURIComponent(file)}`,
      ...(poster ? { poster } : {}),
    };
  });

const fileContents =
  "// AUTO-GENERATED by scripts/build-video-manifest.mjs — do not edit by hand.\n" +
  "// Regenerate with `npm run videos:manifest` (runs automatically on build).\n\n" +
  'import type { VideoManifest } from "@/types/video";\n\n' +
  `export const videoManifest: VideoManifest = ${JSON.stringify(manifest, null, 2)};\n`;

fs.mkdirSync(path.dirname(OUT_FILE), { recursive: true });
fs.writeFileSync(OUT_FILE, fileContents);

const withPoster = manifest.filter((v) => v.poster).length;
console.log(
  `video manifest: ${manifest.length} videos (${withPoster} with posters) -> ${path.relative(projectRoot, OUT_FILE)}`,
);
